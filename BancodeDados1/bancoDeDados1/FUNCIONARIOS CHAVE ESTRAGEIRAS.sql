	CPF(CHAR(11),
    NOME VARCHAR(100),
    PRIMARY KEY(CPF)
);

CREATE TABLE CNH(
		NUM INTEGER PRIMARY KEY,
        CPF CHAR(11),
        foreign key(CPF) REFERENCES PESSOA (CPF),
       UNIQUE(CPF) -- FAZ COM Q FIQUE 1 PRA 1 O RELACIONAMENTO POIS NAO DEIXA REPETIR
);

CREATE TABLE FUNCIONARIOS(
		CPF CHAR(30),
        NOME CHAR(30),
        VAGA INTEGER,
        primary key(CPF)
);

CREATE TABLE DEPARTAMENTOS(
		ID CHAR(30),
        NOME CHAR(30),
        ORCAMENTO INTEGER,
        primary key(ID)
);

CREATE TABLE LOCALIDADES(
		ENDERECO CHAR(30),
        CAPACIDADE CHAR(30),
        primary key(ENDERECO)
);

